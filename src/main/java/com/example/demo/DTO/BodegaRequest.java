package com.example.demo.DTO;

public class BodegaRequest {
	
	
	private Integer idBodegaDTO;
	private String nombreBodegaDTO;
	private String direccionDTO;
	private Integer idProductoDTO;
	public Integer getIdBodegaDTO() {
		return idBodegaDTO;
	}
	public void setIdBodegaDTO(Integer idBodegaDTO) {
		this.idBodegaDTO = idBodegaDTO;
	}
	public String getNombreBodegaDTO() {
		return nombreBodegaDTO;
	}
	public void setNombreBodegaDTO(String nombreBodegaDTO) {
		this.nombreBodegaDTO = nombreBodegaDTO;
	}
	public String getDireccionDTO() {
		return direccionDTO;
	}
	public void setDireccionDTO(String direccionDTO) {
		this.direccionDTO = direccionDTO;
	}
	public Integer getIdProductoDTO() {
		return idProductoDTO;
	}
	public void setIdProductoDTO(Integer idProductoDTO) {
		this.idProductoDTO = idProductoDTO;
	}

	
}
